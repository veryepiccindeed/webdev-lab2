void main() {
  // Jumlah uang yang ditabung setiap hari
  int uangPerHari = 5000;

  // Jumlah hari menabung
  int jumlahHari = 30;

  // Menghitung total uang di celengan setelah 30 hari
  int totalUang = uangPerHari * jumlahHari;

  // Menampilkan total uang di celengan
  print('Total uang di celengan setelah $jumlahHari hari adalah: Rp $totalUang');
}
